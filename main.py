def add(a, b):
    # Adds to number a and b and returns the result
    return a + b


def sub(a, b):
    return a - b


def multiply(a, b):
    return a * b


def divide(a, b):
    return a / b


def main():
    add(1, 2)
    sub(1, 2)
    multiply(1, 2)
    divide(1, 2)


if __name__ == "__main__":
    main()
